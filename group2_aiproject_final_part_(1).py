# ==================================================
# 2. Función de Evaluación de Modelos
# ==================================================

def evaluate_model(y_true, y_pred, y_prob, model_name="Model"):
    """
    Calcula y muestra métricas clave de clasificación y grafica la curva ROC.

    Args:
        y_true: Etiquetas verdaderas.
        y_pred: Etiquetas predichas.
        y_prob: Probabilidades predichas para la clase positiva (1).
        model_name: Nombre del modelo para títulos e impresiones.
    """
    accuracy = accuracy_score(y_true, y_pred)
    # Manejo de posibles problemas con y_prob antes de calcular ROC AUC
    roc_auc = float('nan') # Valor por defecto si no se puede calcular
    if y_prob is not None and isinstance(y_prob, (np.ndarray, pd.Series)) and y_prob.ndim == 1 and not np.isnan(y_prob).any() and np.all(np.isfinite(y_prob)):
        try:
            roc_auc = roc_auc_score(y_true, y_prob)
        except ValueError as e:
            print(f"Advertencia: No se pudo calcular ROC AUC para {model_name}. Error: {e}")
    else:
         print(f"Advertencia: Probabilidades inválidas o ausentes para {model_name}. ROC AUC no se puede calcular.")

    conf_matrix = confusion_matrix(y_true, y_pred)
    class_report = classification_report(y_true, y_pred, zero_division=0) # Añadido zero_division=0

    print(f"\n--- Evaluation Metrics for {model_name} ---")
    print(f"Accuracy: {accuracy:.4f}")
    print(f"ROC AUC Score: {roc_auc:.4f}") # Muestra NaN si no se calculó
    print("\nConfusion Matrix:")
    plt.figure(figsize=(6, 4))
    sns.heatmap(conf_matrix, annot=True, fmt='d', cmap='Blues',
                xticklabels=['Pred Safe (0)', 'Pred Risky (1)'],
                yticklabels=['Actual Safe (0)', 'Actual Risky (1)'])
    plt.ylabel('Actual')
    plt.xlabel('Predicted')
    plt.title(f'Confusion Matrix - {model_name}')
    plt.show() # Muestra el gráfico

    print("\nClassification Report:")
    print(class_report)

    # Graficar Curva ROC solo si AUC es válido
    if not np.isnan(roc_auc) and y_prob is not None:
        fpr, tpr, thresholds = roc_curve(y_true, y_prob)
        plt.figure(figsize=(8, 6))
        plt.plot(fpr, tpr, color='blue', lw=2, label=f'ROC curve (area = {roc_auc:.2f})')
        plt.plot([0, 1], [0, 1], color='grey', lw=2, linestyle='--')
        plt.xlim([0.0, 1.0])
        plt.ylim([0.0, 1.05])
        plt.xlabel('False Positive Rate')
        plt.ylabel('True Positive Rate')
        plt.title(f'Receiver Operating Characteristic (ROC) - {model_name}')
        plt.legend(loc="lower right")
        plt.grid(alpha=0.3)
        plt.show() # Muestra el gráfico
    else:
        print("\nROC Curve cannot be plotted.")

    print("-"*(len(model_name) + 28)) # Línea separadora
    return accuracy, roc_auc # Devuelve métricas clave

